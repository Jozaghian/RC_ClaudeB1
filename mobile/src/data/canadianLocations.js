// Search function with improved matching
export const searchLocations = (query, limit = 20) => {
  if (!query || query.length < 1) return [];

  const searchQuery = query.toLowerCase().trim();
  
  const results = CANADIAN_LOCATIONS
    .filter(city => {
      return city.name.toLowerCase().includes(searchQuery) ||
             city.province.toLowerCase().includes(searchQuery);
    })
    .sort((a, b) => {
      // Exact matches first
      const aExact = a.name.toLowerCase() === searchQuery;
      const bExact = b.name.toLowerCase() === searchQuery;
      if (aExact && !bExact) return -1;
      if (!aExact && bExact) return 1;
      
      // Starts with query second
      const aStarts = a.name.toLowerCase().startsWith(searchQuery);
      const bStarts = b.name.toLowerCase().startsWith(searchQuery);
      if (aStarts && !bStarts) return -1;
      if (!aStarts && bStarts) return 1;
      
      // Alphabetical order
      return a.name.localeCompare(b.name);
    })
    .slice(0, limit);

  return results;
};

export default { CANADIAN_LOCATIONS, searchLocations };